#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*search;

	i = 0;
	search = (unsigned char*)s;
	while (i < n)
	{
		if (search[i] == (unsigned char)c)
			return (&search[i]);
		i++;
	}
	return (NULL);
}
