#include "libft.h"

int		ft_lstsize(t_list *alst)
{
	t_list	*tmp;
	int		i;

	i = 0;
	tmp = alst;
	while (tmp)
	{
		tmp = tmp->next;
		i++;
	}
	return (i);
}
