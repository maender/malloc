#include "libft.h"

int		ft_sqrt(int nb)
{
	int i;

	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	i = 1;
	while (i < nb)
	{
		if ((i * i) >= nb)
			return (i);
		else
			i++;
	}
	return (0);
}
