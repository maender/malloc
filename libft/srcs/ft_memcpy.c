#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t			i;
	unsigned char	*source;
	unsigned char	*temp;

	i = 0;
	temp = (unsigned char*)dst;
	source = (unsigned char*)src;
	while (i < n)
	{
		temp[i] = source[i];
		i++;
	}
	return (dst);
}
